package 스터디예제9;

public class Solution5 {
	int solution(int n) {
	    int answer = 0;
	    int i=0;

	    for(i=1;i<=n;i++){
	        answer++;
	        while(answer%3==0 ||answer%10==3 || (answer/10)%10==3)
	        {
	            answer++;
	        }
	    }

	    return answer;
	}
}


/*이 코드는 3x 마을에서 사용하는 숫자 체계에 따라, 1부터 n까지의 자연수 중 3x 마을에서 사용할 수 있는 숫자의 개수를 구하는 함수입니다.
1부터 n까지의 자연수를 하나씩 확인하면서, 현재 숫자가 3x 마을에서 사용할 수 있는 숫자인지를 판별하고, 사용할 수 없는 경우에는 다음 숫자로 넘어가는 방식으로 구현되어 있습니다.

구체적으로, 변수 answer를 1로 초기화하고, 1부터 n까지의 숫자를 하나씩 확인합니다. 이때 while문을 사용하여, answer가 3x 마을에서 사용할 수 없는 숫자인 경우 다음 숫자로 넘어갑니다.

while문 안에서는, answer가 3의 배수이거나 일의 자리가 3인 경우, 또는 십의 자리가 3인 경우에 answer를 1 증가시켜서 다음 숫자를 확인합니다. 이렇게 하면, 3x 마을에서 사용할 수 있는 숫자인 경우에는 answer를 유지하면서 다음 숫자를 확인할 수 있습니다.

for문이 끝난 후에는, answer를 반환하여 1부터 n까지의 자연수 중 3x 마을에서 사용할 수 있는 숫자의 개수를 구합니다.
*/
